package ezequiel.whatthefridge.Objetos;

public class TablaNutricional {
    private float carbohidratos;
    private float grasas;
    private float proteinas;
    private float calorias;
    private float macroNutrientes;

    public float getCarbohidratos() {
        return carbohidratos;
    }

    public void setCarbohidratos(float carbohidratos) {
        this.carbohidratos = carbohidratos;
    }

    public float getGrasas() {
        return grasas;
    }

    public void setGrasas(float grasas) {
        this.grasas = grasas;
    }

    public float getProteinas() {
        return proteinas;
    }

    public void setProteinas(float proteinas) {
        this.proteinas = proteinas;
    }

    public float getCalorias() {
        return calorias;
    }

    public void setCalorias(float calorias) {
        this.calorias = calorias;
    }

    public float getMacroNutrientes() {
        return macroNutrientes;
    }

    public void setMacroNutrientes(float macroNutrientes) {
        this.macroNutrientes = macroNutrientes;
    }

}
